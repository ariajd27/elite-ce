const unsigned char sin_lut[32] = {
  0x00, 0x19, 0x32, 0x4a, 0x62, 0x79, 0x8e, 0xa2, 
  0xb5, 0xc6, 0xd5, 0xe2, 0xed, 0xf5, 0xfb, 0xff, 
  0xff, 0xff, 0xfb, 0xf5, 0xed, 0xe2, 0xd5, 0xc6,
  0xb5, 0xa2, 0x8e, 0x79, 0x62, 0x4a, 0x32, 0x19
};

const unsigned char atan_lut[32] = {
  0x00, 0x01, 0x03, 0x04, 0x05, 0x06, 0x08, 0x09, 
  0x0a, 0x0b, 0x0c, 0x0d, 0x0f, 0x10, 0x11, 0x12, 
  0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x19,
  0x1a, 0x1b, 0x1c, 0x1d, 0x1d, 0x1e, 0x1f, 0x1f
};

signed int trig_sin(const unsigned char angle)
{
	if (angle < 32) return sin_lut[angle]; // Q1, Q2
	else return -1 * sin_lut[angle - 32]; // Q3, Q4
}

signed int trig_cos(const unsigned char angle)
{
	if (angle < 16) return sin_lut[angle + 16]; // Q1
	else if (angle < 48) return -1 * sin_lut[angle - 16]; // Q2, Q3
	else return sin_lut[angle - 48]; // Q4
}
